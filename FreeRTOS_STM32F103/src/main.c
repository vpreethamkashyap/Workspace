SemaphoreHandle_t xSemaphore = NULL;

int main( void )
{

	volatile BaseType_t xReturnedTASK1;
	volatile BaseType_t xReturnedTASK2;
  
	/* essential Board initializations */
   SystemInit();
	
	 /* This function initializes the MCU clock, PLL will be used to generate Main MCU clock */
	//SystemCoreClockConfigure();
	SystemCoreClockUpdate();
	
	/* Trace init */
	 
	
	trace_printf("Eclipse-FreeRTOS Project, Feb 11 , 2017 \n");
	
	
	/* Create one of the two tasks. */
	xReturnedTASK1 = xTaskCreate(	vTask1,		/* Pointer to the function that implements the task. */
									"Task 1",	            /* Text name for the task.  This is to facilitate debugging only. */
									240,						/* Stack depth in words. */
									NULL,					/* We are not using the task parameter. */
									1,							/* This task will run at priority 1. */
									NULL );				/* We are not using the task handle. */

	/* Create the other task in exactly the same way. */
	xReturnedTASK2 = xTaskCreate( vTask2, "Task 2", 240, NULL, 1, NULL );
	
	/* Create the other task in exactly the same way. */
	//xTaskCreate( vTask2, "crazy1", 240, NULL, 1, NULL );
	
	/* Create the other task in exactly the same way. */
	//xTaskCreate( vTask2, "crazy2", 240, NULL, 1, NULL );
	
  /* lets create the binary semaphore dynamically */
	xSemaphore = xSemaphoreCreateBinary();
	
	/* lets make the semaphore token available for the first time */
	xSemaphoreGive( xSemaphore);

	/* Start the scheduler so our tasks start executing. */
	vTaskStartScheduler();

	/* If all is well we will never reach here as the scheduler will now be
	running.  If we do reach here then it is likely that there was insufficient
	heap available for the idle task to be created. */
	for( ;; );
}
/*-----------------------------------------------------------*/

void vTask1( void *pvParameters )
{
const char *pcTaskName = "Task 1 is running\n";
volatile unsigned long ul;

	/* As per most tasks, this task is implemented in an infinite loop. */
	for( ;; )
	{
		/* Print out the name of this task. */
		/* lets make the sema un-available */
	
		 xSemaphoreTake( xSemaphore, ( TickType_t ) portMAX_DELAY );
		 trace_printf( "%s\n",pcTaskName );
		/* lets make the sema available */
		 xSemaphoreGive( xSemaphore);

		/* Delay for a period. */
		for( ul = 0; ul < mainDELAY_LOOP_COUNT; ul++ )
		{
			/* This loop is just a very crude delay implementation.  There is
			nothing to do in here.  Later exercises will replace this crude
			loop with a proper delay/sleep function. */
		}
		taskYIELD();
	}
}
